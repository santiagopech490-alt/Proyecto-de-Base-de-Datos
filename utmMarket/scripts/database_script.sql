-- Eliminar las tablas si ya existen para asegurar una ejecución limpia
IF OBJECT_ID('DetalleDeVenta', 'U') IS NOT NULL
    DROP TABLE DetalleDeVenta;
IF OBJECT_ID('Venta', 'U') IS NOT NULL
    DROP TABLE Venta;
IF OBJECT_ID('Producto', 'U') IS NOT NULL
    DROP TABLE Producto;
GO

-- 1. Tabla Producto
CREATE TABLE Producto (
    IdProducto INT IDENTITY(1,1) PRIMARY KEY,
    Nombre NVARCHAR(255) NOT NULL,
    Marca NVARCHAR(255) NOT NULL,
    Precio DECIMAL(10, 2) NOT NULL,
    Stock INT NOT NULL
);
GO

-- 2. Tabla Venta
CREATE TABLE Venta (
    Folio INT IDENTITY(1,1) PRIMARY KEY,
    FechaDeVenta DATETIME DEFAULT GETDATE(),
    TotalDeLaVenta DECIMAL(10, 2) NOT NULL DEFAULT 0.00
);
GO

-- 3. Tabla DetalleDeVenta
CREATE TABLE DetalleDeVenta (
    IdDetalleVenta INT IDENTITY(1,1) PRIMARY KEY,
    FolioVenta INT NOT NULL,
    IdProducto INT NOT NULL,
    Cantidad INT NOT NULL,
    PrecioUnitario DECIMAL(10, 2) NOT NULL,
    TotalDeDetalle DECIMAL(10, 2) NOT NULL,
    CONSTRAINT FK_Venta FOREIGN KEY (FolioVenta) REFERENCES Venta(Folio),
    CONSTRAINT FK_Producto FOREIGN KEY (IdProducto) REFERENCES Producto(IdProducto)
);
GO

